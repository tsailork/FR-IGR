def get_pressure(U, gamma=1.4):
    """Computes physical thermodynamic pressure."""
    rho = U[0]
    rhou = U[1]
    E = U[2]
    return (gamma - 1.0) * (E - 0.5 * rhou**2 / rho)

def get_entropy(U, gamma=1.4):
    """Computes physical entropy s = ln(P/rho^gamma)."""
    rho = U[0]
    P = get_pressure(U, gamma)
    # Clip for safety before log
    return np.log(np.maximum(P, 1e-14) / np.maximum(rho, 1e-14)**gamma)

def get_flux(U, theta, gamma=1.4):
    """
    Computes the 1D physical flux vector incorporating regularized pressure
    for the momentum and energy equations.
    """
    rho = U[0]
    rhou = U[1]
    E = U[2]
    S = U[3] # S = rho * P_phan

    P_phys = (gamma - 1.0) * (E - 0.5 * rhou**2 / rho)
    P_reg = P_phys + theta * (P_phys - S / rho)

    F = np.zeros_like(U)
    F[0] = rhou
    F[1] = rhou**2 / rho + P_reg
    F[2] = (E + P_reg) * (rhou / rho)
    F[3] = rhou * (S / rho)
    return F, P_reg, P_phys

