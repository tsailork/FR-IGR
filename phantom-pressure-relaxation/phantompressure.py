def run_solver(case_name="sod", N_cells=150, N=2, C_tau=0.2, theta=1.0, cfl=0.15):
    """
    Executes the Euler solver with SSPRK3 time integration and Operator Splitting
    for the non-equilibrium phantom pressure.
    """
    gamma = 1.4
    r, w, D, V_L, V_R, g_L_prime, g_R_prime = setup_fr(N)
    Np = N + 1

    # Spatial domain setup
    if case_name == "sod":
        x_min, x_max = 0.0, 1.0
        t_end = 0.20
    elif case_name == "shu_osher":
        x_min, x_max = -5.0, 5.0
        t_end = 1.80
    else:
        raise ValueError("Unknown test case.")

    dx = (x_max - x_min) / N_cells
    x_faces = np.linspace(x_min, x_max, N_cells + 1)
    x_sol = np.zeros((N_cells, Np))
    for i in range(N_cells):
        x_sol[i, :] = x_faces[i] + 0.5 * (1.0 + r) * dx

    # Initial Conditions
    rho_init = np.zeros_like(x_sol)
    u_init = np.zeros_like(x_sol)
    P_init = np.zeros_like(x_sol)

    # Smoothing factor
    delta = dx * 0.1

    if case_name == "sod":
        x0 = 0.5
        S = 1.0 / (1.0 + np.exp((x_sol - x0) / (delta / 5.0)))
        rho_init = 0.125 + (1.0 - 0.125) * S
        P_init = 0.1 + (1.0 - 0.1) * S
        u_init[:] = 0.0
    elif case_name == "shu_osher":
        x0 = -4.0
        S = 1.0 / (1.0 + np.exp((x_sol - x0) / (delta / 5.0)))
        rho_jump = 3.857143
        u_jump = 2.629369
        P_jump = 10.33333

        rho_init = (1.0 + 0.2 * np.sin(5.0 * x_sol)) * (1.0 - S) + rho_jump * S
        u_init = 0.0 * (1.0 - S) + u_jump * S
        P_init = 1.0 * (1.0 - S) + P_jump * S

    # Set conserved variables: [rho, rho*u, E, rho*P_phan]
    U = np.zeros((4, N_cells, Np))
    U[0] = rho_init
    U[1] = rho_init * u_init
    U[2] = P_init / (gamma - 1.0) + 0.5 * rho_init * u_init**2
    U[3] = rho_init * P_init

    U = apply_zhang_shu_limiter(U, w, V_L, V_R, N_cells, Np, gamma)
    U = apply_entropy_limiter(U, w, N_cells, gamma)

    # Store fixed boundary values from initial state
    U_fixed_L = np.einsum('j,cj->c', V_L, U[:, 0, :])
    U_fixed_R = np.einsum('j,cj->c', V_R, U[:, -1, :])

    t = 0.0
    step = 0
    u_save = [U.copy()]
    t_save = [t]

    print(f"\n--- Initiating Run: {case_name.upper()} (Cells={N_cells}, Degree={N}) ---")

    while t < t_end:
        # Time-step calculation
        u = U[1] / U[0]
        P_phys = get_pressure(U, gamma)
        a = np.sqrt(gamma * np.maximum(P_phys, 1e-12) / U[0])
        lambda_max = np.max(np.abs(u) + a)

        dt = cfl * dx / ((2 * N + 1) * lambda_max)
        if t + dt > t_end:
            dt = t_end - t

        # 1. Compute non-equilibrium relaxation scale tau
        tau_cell = compute_tau(U, dx, C_tau, w, gamma)

        # 2. Time step: Pure Advection (SSPRK3)
        # Stage 1
        RHS1 = compute_rhs(U, dx, N_cells, Np, r, w, D, V_L, V_R, g_L_prime, g_R_prime, theta, gamma, U_fixed_L, U_fixed_R)
        U1 = U - dt * RHS1
        U1 = apply_zhang_shu_limiter(U1, w, V_L, V_R, N_cells, Np, gamma)
        U1 = apply_entropy_limiter(U1, w, N_cells, gamma)

        # Intermediate Relaxation for Stage 1
        tau_cell1 = compute_tau(U1, dx, C_tau, w, gamma)
        U1 = relax_step(U1, dt, tau_cell1, gamma)  # <-- Relax here!

        # Stage 2
        RHS2 = compute_rhs(U1, dx, N_cells, Np, r, w, D, V_L, V_R, g_L_prime, g_R_prime, theta, gamma, U_fixed_L, U_fixed_R)
        U2 = 0.75 * U + 0.25 * (U1 - dt * RHS2)
        U2 = apply_zhang_shu_limiter(U2, w, V_L, V_R, N_cells, Np, gamma)
        U2 = apply_entropy_limiter(U2, w, N_cells, gamma)

        # Intermediate Relaxation for Stage 2
        tau_cell2 = compute_tau(U2, dx, C_tau, w, gamma)
        U2 = relax_step(U2, 0.25 * dt, tau_cell2, gamma)

        # Stage 3
        RHS3 = compute_rhs(U2, dx, N_cells, Np, r, w, D, V_L, V_R, g_L_prime, g_R_prime, theta, gamma, U_fixed_L, U_fixed_R)
        U_new = (1.0/3.0) * U + (2.0/3.0) * (U2 - dt * RHS3)
        U_new = apply_zhang_shu_limiter(U_new, w, V_L, V_R, N_cells, Np, gamma)
        U_new = apply_entropy_limiter(U_new, w, N_cells, gamma)

        # Intermediate Relaxation for Stage 3
        tau_cell3 = compute_tau(U_new, dx, C_tau, w, gamma)
        U_new = relax_step(U_new, (2.0/3.0) * dt, tau_cell3, gamma)

        #    # 3. Time step: Local Analytical Relaxation Step
        #    U_new = relax_step(U_new, dt, tau_cell, gamma)

        U = U_new.copy()
        t += dt
        step += 1

        if step % 100 == 0 or t >= t_end:
            u_save.append(U.copy())
            t_save.append(t)

        if step % 500 == 0 or t >= t_end:
            print(f"Iteration: {step:4d} | Simulated Time: {t:6.4f}s | Step Size dt: {dt:6.4e}s")

    return x_sol.flatten(), u_save, t_save, gamma, theta

