def apply_zhang_shu_limiter(U, w, V_L, V_R, N_cells, Np, gamma=1.4):
    """
    UPDATED: Vectorized Zhang-Shu limiter that checks both internal solution
    points AND extrapolated interface points to prevent Riemann solver crashes.
    Features a numerically stable quadratic root finder.
    """
    eps = 1e-12
    U_avg = np.sum(U * w[None, None, :], axis=2)

    # Helper to get all points (internal + boundaries) to check for positivity
    def get_all_points(U_curr):
        U_L = np.einsum('j,cij->ci', V_L, U_curr)
        U_R = np.einsum('j,cij->ci', V_R, U_curr)
        return np.concatenate([U_curr, U_L[:, :, None], U_R[:, :, None]], axis=2)

    U_check = get_all_points(U)

    # ---- DENSITY LIMITING ----
    rho_min = np.min(U_check[0, :, :], axis=1)
    theta_rho = np.ones(N_cells)
    idx = rho_min < eps

    if np.any(idx):
        denom = U_avg[0, idx] - rho_min[idx]
        denom = np.where(denom < 1e-14, 1.0, denom)
        theta_rho[idx] = (U_avg[0, idx] - eps) / denom
        theta_rho = np.clip(theta_rho, 0.0, 1.0)

        for c in range(4):
            U[c, :, :] = U_avg[c, :, None] + theta_rho[:, None] * (U[c, :, :] - U_avg[c, :, None])

    # Recompute averages and boundaries after density limiting
    U_avg = np.sum(U * w[None, None, :], axis=2)
    U_check = get_all_points(U)

    # ---- PRESSURE LIMITING ----
    theta_P = np.ones(N_cells)
    # Loop over internal points (Np) AND the 2 boundary points
    for j in range(Np + 2):
        U_j = U_check[:, :, j]
        P_j = get_pressure(U_j, gamma)
        idx = P_j < eps

        if np.any(idx):
            rho_avg = U_avg[0, idx]
            rhou_avg = U_avg[1, idx]
            E_avg = U_avg[2, idx]
            E_eps_avg = E_avg - eps / (gamma - 1.0)

            d_rho = U_j[0, idx] - rho_avg
            d_rhou = U_j[1, idx] - rhou_avg
            d_E = U_j[2, idx] - E_avg

            a_q = d_rho * d_E - 0.5 * d_rhou**2
            b_q = rho_avg * d_E + E_eps_avg * d_rho - rhou_avg * d_rhou
            c_q = rho_avg * E_eps_avg - 0.5 * rhou_avg**2

            # Stable quadratic root finding
            disc = np.maximum(b_q**2 - 4.0 * a_q * c_q, 0.0)
            sign_b = np.where(b_q >= 0, 1.0, -1.0)
            q = -0.5 * (b_q + sign_b * np.sqrt(disc))

            is_linear = np.abs(a_q) < 1e-14
            t1 = np.zeros_like(a_q)
            t2 = np.zeros_like(a_q)

            q_safe = np.where(np.abs(q) < 1e-14, 1e-14, q)
            t1[~is_linear] = q[~is_linear] / a_q[~is_linear]
            t2[~is_linear] = c_q[~is_linear] / q_safe[~is_linear]

            t_linear = np.zeros_like(a_q)
            b_safe = np.where(np.abs(b_q) < 1e-14, 1.0, b_q)
            t_linear[is_linear] = -c_q[is_linear] / b_safe[is_linear]

            def get_valid_root(r1, r2):
                v1 = np.where((r1 >= -1e-8) & (r1 <= 1.0 + 1e-8), r1, 10.0)
                v2 = np.where((r2 >= -1e-8) & (r2 <= 1.0 + 1e-8), r2, 10.0)
                return np.minimum(v1, v2)

            t_sel = np.where(is_linear, t_linear, get_valid_root(t1, t2))
            t_sel = np.clip(t_sel, 0.0, 1.0)

            theta_P[idx] = np.minimum(theta_P[idx], t_sel)

    for c in range(4):
        U[c, :, :] = U_avg[c, :, None] + theta_P[:, None] * (U[c, :, :] - U_avg[c, :, None])

    return U

def apply_entropy_limiter(U, w, N_cells, gamma=1.4):
    """
    Local Minimum Entropy Limiter based on bisection search.
    Ensures cell entropy >= min(entropy of cell and its neighbors).
    """
    U_avg = np.sum(U * w[None, None, :], axis=2)
    s_avg_all = get_entropy(U_avg, gamma)

    # Define local minimum entropy targets (3-cell stencil)
    s_min_target = np.zeros(N_cells)
    for i in range(N_cells):
        im1 = max(0, i-1)
        ip1 = min(N_cells-1, i+1)
        s_min_target[i] = min(s_avg_all[im1], s_avg_all[i], s_avg_all[ip1])

    tol = 1e6    # ATTENTION: This tolerance is set to disable the entropy limiter entirely
    max_iter = 8

    # Check which cells violate the local minimum entropy
    s_points = get_entropy(U, gamma)
    s_min_points = np.min(s_points, axis=1)
    violating_cells = s_min_points < (s_min_target - tol)

    if not np.any(violating_cells):
        return U

    # Bisection search to find theta for U_limited = U_avg + theta * (U - U_avg)
    theta_L = np.zeros(N_cells)
    theta_R = np.ones(N_cells)
    theta = np.ones(N_cells)

    for _ in range(max_iter):
        theta = 0.5 * (theta_L + theta_R)
        # Test conservative state
        U_test = U_avg[:, :, None] + theta[None, :, None] * (U - U_avg[:, :, None])
        s_test = get_entropy(U_test, gamma)
        s_min_test = np.min(s_test, axis=1)

        success = s_min_test >= (s_min_target - tol)
        theta_L = np.where(success, theta, theta_L)
        theta_R = np.where(~success, theta, theta_R)

    # Final update for all violating cells
    theta_final = theta_L
    for c in range(4):
        U[c, violating_cells, :] = U_avg[c, violating_cells, None] + theta_final[violating_cells, None] * (U[c, violating_cells, :] - U_avg[c, violating_cells, None])

    return U

