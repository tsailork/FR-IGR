def legendre_poly_and_deriv(n, x):
    """
    Evaluates the Legendre polynomial P_n(x) and its derivative P'_n(x)
    using stable recurrence relations.
    """
    if n == 0:
        return 1.0, 0.0
    elif n == 1:
        return x, 1.0
    p0, dp0 = 1.0, 0.0
    p1, dp1 = x, 1.0
    for k in range(2, n + 1):
        pk = ((2*k - 1)*x*p1 - (k - 1)*p0) / k
        dpk = dp0 + (2*k - 1)*p1
        p0, p1 = p1, pk
        dp0, dp1 = dp1, dpk
    return p1, dp1

def setup_fr(N):
    """
    Sets up Flux Reconstruction matrices and vectors for polynomial degree N.
    Uses Gauss-Legendre points as solution points.
    """
    r, w = np.polynomial.legendre.leggauss(N + 1)
    w = w / 2.0  # Normalize weights to sum to 1.0 (on domain length of 2)
    Np = N + 1

    # Precompute derivative matrix D (D_ij = l_j'(r_i)) & boundary extrapolators
    D = np.zeros((Np, Np))
    V_L = np.zeros(Np)
    V_R = np.zeros(Np)

    for j in range(Np):
        # Evaluate Lagrange basis at borders r = -1 and r = 1
        num_L, den = 1.0, 1.0
        num_R = 1.0
        for k in range(Np):
            if k != j:
                num_L *= (-1.0 - r[k])
                num_R *= (1.0 - r[k])
                den *= (r[j] - r[k])
        V_L[j] = num_L / den
        V_R[j] = num_R / den

        # Calculate derivatives
        for i in range(Np):
            if i != j:
                prod = 1.0
                for k in range(Np):
                    if k != j and k != i:
                        prod *= (r[i] - r[k])
                D[i, j] = prod / den
            else:
                s = sum(1.0 / (r[j] - r[k]) for k in range(Np) if k != j)
                D[i, j] = s

    # Radau correction function derivatives for DG equivalence
    g_L_prime = np.zeros(Np)
    g_R_prime = np.zeros(Np)
    for i in range(Np):
        _, dP_N = legendre_poly_and_deriv(N, r[i])
        _, dP_Np1 = legendre_poly_and_deriv(N + 1, r[i])
        g_L_prime[i] = ((-1.0)**N / 2.0) * (dP_N - dP_Np1)
        g_R_prime[i] = (0.5) * (dP_N + dP_Np1)

    return r, w, D, V_L, V_R, g_L_prime, g_R_prime

