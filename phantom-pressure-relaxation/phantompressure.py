def compute_rhs(U, dx, N_cells, Np, r, w, D, V_L, V_R, g_L_prime, g_R_prime, theta, gamma, U_fixed_L=None, U_fixed_R=None):
    """
    Computes the spatial Flux Reconstruction derivative operator dF/dx.
    Updated to handle fixed constant boundary condition on both sides.
    """
    # Extrapolate internal states to boundaries (Interfaces 0 and N_cells)
    U_L = np.einsum('j,cij->ci', V_L, U)
    U_R = np.einsum('j,cij->ci', V_R, U)

    # Establish interface states
    U_int_L = np.zeros((4, N_cells + 1))
    U_int_R = np.zeros((4, N_cells + 1))
    U_int_L[:, 1:-1] = U_R[:, :-1]
    U_int_R[:, 1:-1] = U_L[:, 1:]

    # ----------------- LEFT BOUNDARY (Interface 0) -----------------
    # Interior state is on the Right of Interface 0
    U_int_R[:, 0] = U_L[:, 0]

    if U_fixed_L is not None:
        # Weak Dirichlet / Inflow Boundary
        U_int_L[:, 0] = U_fixed_L
    else:
        # Supersonic/Extrapolated Inflow
        U_int_L[:, 0] = U_L[:, 0]


    # ----------------- RIGHT BOUNDARY (Interface -1) -----------------
    # Interior state is on the Left of Interface N_cells (index -1)
    U_int_L[:, -1] = U_R[:, -1]

    if U_fixed_R is not None:
        # WEAK SUBSONIC OUTFLOW: Extrapolate rho and momentum, impose pressure
        rho_int = U_R[0, -1]
        rhou_int = U_R[1, -1]

        # Target physical pressure from your fixed state
        P_out = (gamma - 1.0) * (U_fixed_R[2] - 0.5 * U_fixed_R[1]**2 / U_fixed_R[0])

        # Construct the Ghost State (U_int_R)
        U_ghost = np.zeros(4)
        U_ghost[0] = rho_int  # Extrapolate density
        U_ghost[1] = rhou_int # Extrapolate momentum
        U_ghost[2] = P_out / (gamma - 1.0) + 0.5 * rhou_int**2 / rho_int # Impose pressure
        U_ghost[3] = rho_int * P_out # Sync the phantom pressure to avoid artificial regularized jumps

        U_int_R[:, -1] = U_ghost
    else:
        # WEAK SUPERSONIC OUTFLOW: Simple extrapolation
        # Set ghost state slightly offset or matching to allow the Riemann solver to stay active
        U_int_R[:, -1] = U_R[:, -1]

    # Interface flux calculations
    F_int_L, Preg_int_L, P_int_L = get_flux(U_int_L, theta, gamma)
    F_int_R, Preg_int_R, P_int_R = get_flux(U_int_R, theta, gamma)

    u_int_L = U_int_L[1] / U_int_L[0]
    u_int_R = U_int_R[1] / U_int_R[0]
    a_int_L = np.sqrt(gamma * np.maximum(Preg_int_L, P_int_L) / U_int_L[0])
    a_int_R = np.sqrt(gamma * np.maximum(Preg_int_R, P_int_R) / U_int_R[0])

    C_max = np.maximum(np.abs(u_int_L) + a_int_L, np.abs(u_int_R) + a_int_R)
    F_com = 0.5 * (F_int_L + F_int_R) - 0.5 * C_max[None, :] * (U_int_R - U_int_L)

    # Compute fluxes inside elements
    F_sol, _, _ = get_flux(U, theta, gamma)
    F_L = np.einsum('j,cij->ci', V_L, F_sol)
    F_R = np.einsum('j,cij->ci', V_R, F_sol)

    # Reconstruct spatial derivative
    RHS = np.einsum('jk,cik->cij', D, F_sol)
    RHS += np.einsum('ci,j->cij', F_com[:, :-1] - F_L, g_L_prime)
    RHS += np.einsum('ci,j->cij', F_com[:, 1:] - F_R, g_R_prime)
    RHS *= (2.0 / dx)

    return RHS

def compute_tau(U, dx, C_tau, w, gamma=1.4):
    """Computes the local non-equilibrium relaxation time scale tau per cell."""
    U_avg = np.sum(U * w[None, None, :], axis=2)
    rho_avg = U_avg[0]
    u_avg = U_avg[1] / rho_avg
    P_avg = np.maximum(get_pressure(U_avg, gamma), 1e-12)
    a_avg = np.sqrt(gamma * P_avg / rho_avg)
    return C_tau * dx / (a_avg + np.abs(u_avg) + 1e-12)

def relax_step(U, dt, tau_cell, gamma=1.4):
    """Applies the analytical operator splitting solution to update phantom pressure."""
    rho = U[0]
    rhou = U[1]
    E = U[2]
    S = U[3]

    P_phys = (gamma - 1.0) * (E - 0.5 * rhou**2 / rho)
    exp_factor = np.exp(-dt / tau_cell)[:, None]

    U[3] = rho * P_phys + (S - rho * P_phys) * exp_factor
    return U

